package APS1UNID;

public class Medico {

	private String cpfmed, crm, nomemed;
	private int datanascimento, datacadastro;
	
	public String getCpfmed() {
		return cpfmed;
	}
	public void setCpfmed(String cpfmed) {
		this.cpfmed = cpfmed;
	}
	public String getCrm() {
		return crm;
	}
	public void setCrm(String crm) {
		this.crm = crm;
	}
	public String getNomemed() {
		return nomemed;
	}
	public void setNomemed(String nomemed) {
		this.nomemed = nomemed;
	}
	public int getDatanascimento() {
		return datanascimento;
	}
	public void setDatanascimento(int datanascimento) {
		this.datanascimento = datanascimento;
	}
	public int getDatacadastro() {
		return datacadastro;
	}
	public void setDatacadastro(int datacadastro) {
		this.datacadastro = datacadastro;
	}
	
	
	
	
}
